                <ol>
                    <li>besorgt einen/eine Ersatzschiedsrichter_in für Euren Termin, die Kontaktdaten findet Ihr auf der VSR-Liste</li>
                    <li>gebt den Tausch per E-Mail oder Telefon bekannt an:
                        <ul>
                            <li>den 
                                <a href="/personen.html">VSRA</a> (Alexander Ohle, Martin Becker oder Ekkart Kleinod)
                            </li>
                            <li>den jeweiligen Ansprechpartner des ausrichtenden Vereins</li>
                            <li>Eure/Euren Ersatzschiedsrichter_in</li>
                        </ul>
                    </li>
                    <li>fertig.</li>
                    <li>falls Ihr keinen Ersatz finden könnt, informiert den 
                        <a href="/personen.html">VSRA</a> (Alexander Ohle, Martin Becker oder Ekkart Kleinod)
                    </li>
                </ol>

                <p><em>Unser Ziel ist, dass zu jedem Spiel ein Oberschiedsrichter und die Schiedsrichter pünktlich erscheinen. Bei Verhinderung haltet Euch rechtzeitig an die vorgegebenen 4 Punkte, damit gewährleistet ist, dass kein Spiel ohne OSR bzw. SR stattfinden muss.</em>
                </p>

